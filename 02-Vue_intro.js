// Vue
// 构建用户界面的前端框架

// 特性
// 1.数据驱动视图
//   vue监听数据的变化 自动渲染页面的结构
//   数据驱动视图是单向的数据绑定
//   当页面数据发生变化时自动重新渲染
// 2.双向数据绑定
//   辅助开发者在不操作DOM的情况下，自动的把用户填写的内容
//   同步到数据源中

// MVVM
// 特性的核心原理
// Model View ViewModel
// 每个页面拆分为这三个部分
// Model:渲染所需的数据源
// View:当前页面渲染的DOM结构
// ViewModel:vue的实例 MVVM的核心
// ViewModel作为MVVM的核心 它把当前页面的数据源Model和页面结构View绑定到一起
